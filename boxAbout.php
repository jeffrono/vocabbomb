			<span class="xbig">Why is VocabBomb so special?</span>
			<p>
			Vocabbomb is an insanely addictive, simple twitter-based game. Check today's <a href="challenge.php">challenge word</a>. Use it in an interesting tweet. <a href="rate.php">Rate</a> other vocab bombs.
			<p>
			<span class="red">Learn words by using them: </span>
			You can study all the flash cards you want but the best way to really learn a word is to write a great sentence using it.
			<p>
			<span class="red">Get your creative juices flowing: </span>

			They say you should write something creative every day.  VocabBomb is the simplest way to do that.  It's not so easy to come up with a funny/sarcastic/pithy/witty/clever sentence that uses "inured" is it?
			<p>
			<span class="red">Be proud of a quality tweet: </span>

			Most of your Twitter stream is probably filled with mind-numbing checkins, blah retweets, shortened URLS and tons of ugly hashtags.  It's no reason that most of Twitter is filled with zombie accounts!  Let's change that by adding a few shiny tweets that stand out and represent some quality writing and thinking.
			<p>
			<span class="red">Discover great tweeters who aren't Ashton Kutcher: </span>

			Believe it or not, there are people on Twitter (who aren't Stephen Colbert) who consistently write funny/interesting/clever tweets.  With the simple VocabBomb rating system, you can quickly see who's writing the best material.  People who write highly rated tweets on VocabBomb tend to have consistently interesting tweets.  Follow them!
			<p>
			<span class="red">Oh yeah... POINTS!: </span>

			Who doesn't love points?  Anyone who comes to VocabBomb can <a href="rate.php">rate your tweets</a> with a +1, 0 or -1.  So each of your tweets has a rating count, and you get a total score from your tweets.  <a href="toptweets.php">Top tweets</a> are displayed prominantly, as are <a href="scoreboard.php">players who write the best tweets</a>. 
			<p>
			<span class="red">No registration, No log in: </span>

			Participating could not be easier... seriously.  All you need is a <a href="http://www.twitter.com">twitter account</a>.  Your tweet only needs two ingredients to be displayed on VocabBomb: 1) use the challenge word and 2) include #vocabbomb.  That's it.  Your twitter username will be automatically added to the site if your tweet is indexed and then you'll be right up there on the <a href="scoreboard.php">leaderboard</a>.
